/**
 * GET /api/analytics/dashboard
 * Returns dashboard stats for current tenant
 */
export const getDashboardStats = async (req, res) => {
    const tenantId = req.tenantId;

    const [websiteCount, pageCount, aiUsageCount, deploymentCount, userCount, recentActivity, recentDeployments] =
        await Promise.all([
            Website.countDocuments({ tenantId }),
            Page.countDocuments({ tenantId }),
            AIUsageLog.countDocuments({ tenantId }),
            Deployment.countDocuments({ tenantId }),
            User.countDocuments({ tenantId, isActive: true }),
            ActivityLog.find({ tenantId })
                .populate("userId", "name email")
                .sort({ createdAt: -1 })
                .limit(20)
                .lean(),
            Deployment.find({ tenantId })
                .populate("deployedBy", "name email")
                .populate("websiteId", "name")
                .sort({ createdAt: -1 })
                .limit(10)
                .lean(),
        ]);

    res.json({
        success: true,
        stats: {
            websiteCount,
            pageCount,
            aiUsageCount,
            deploymentCount,
            userCount,
        },
        recentActivity,
        recentDeployments,
    });
};

/**
 * GET /api/analytics/activity
 */
export const getActivityLogs = async (req, res) => {
    const { page = 1, limit = 20 } = req.query;
    const skip = (parseInt(page) - 1) * parseInt(limit);

    const [logs, total] = await Promise.all([
        ActivityLog.find({ ...req.tenantFilter })
            .populate("userId", "name email")
            .sort({ createdAt: -1 })
            .skip(skip)
            .limit(parseInt(limit))
            .lean(),
        ActivityLog.countDocuments({ ...req.tenantFilter }),
    ]);

    res.json({ success: true, logs, total, page: parseInt(page), limit: parseInt(limit) });
};

/**
 * GET /api/analytics/website/:websiteId
 * Returns advanced analytics generated by AI based on form submissions
 */
export const getWebsiteAnalytics = async (req, res) => {
    try {
        const { websiteId } = req.params;
        const tenantId = req.tenantId;

        // Verify website ownership
        const website = await Website.findOne({ _id: websiteId, tenantId }).populate("tenantId");
        if (!website) {
            return res.status(404).json({ success: false, message: "Website not found or access denied" });
        }

        // Generate slugs
        const tenantSlug = website.tenantId.slug || tenantId;
        const websiteSlug = website.slug || website.name?.toLowerCase().replace(/[^a-z0-9]+/g, '-') || websiteId;

        // Fetch form submissions
        console.log(`\n📡 [Analytics] Fetching remote forms data from Firebase for ${website.name}...`);
        const submissions = await firebaseService.getFormSubmissions(tenantSlug, websiteSlug, 500);
        console.log(`🔥 [Firebase] Successfully loaded ${submissions.length} dynamic form submissions!`);

        // --- REAL visitor stats from Firebase + Socket.IO ---
        // Total page views stored persistently in Firestore
        const { totalPageViews } = await firebaseService.getAnalyticsTotals(tenantSlug, websiteSlug);
        const totalVisitors = totalPageViews;

        // Live visitors = sockets currently in the visitor room for this websiteId
        const websiteIdStr = website._id.toString();
        const liveUsers = liveVisitors.get(websiteIdStr)?.size ?? 0;

        let insights = [];
        let summary = "Not enough data for insights.";

        if (submissions.length > 0) {
            try {
                // Ask Qwen to analyze the form submissions
                const prompt = `
Analyze these form submissions and give me exactly 3 bullet-point insights about business trends.
Data: ${JSON.stringify(submissions.slice(0, 50))}
Return JSON: { "summary": "2 sentences", "insights": [{ "label": "Title", "value": "Metric", "insightText": "Short text" }] }
`;
                console.log(`🧠 [Qwen AI] Generating Lightning-Fast Insights on ${submissions.length} records...`);
                const result = await ollamaClient.chat({
                    model: 'qwen2.5:3b',
                    messages: [
                        { role: 'system', content: 'You are a fast data analyzer. Return raw JSON only.' },
                        { role: 'user', content: prompt }
                    ],
                    format: 'json',
                    options: { 
                        temperature: 0.1,
                        num_predict: 500, // Speed limit
                        num_ctx: 2048 
                    }
                });
                const responseText = result.message.content;
                console.log(`✅ [Qwen AI] Successfully derived insights with Smart-Balanced AI!`);
                let jsonStr = responseText;
                const jsonMatch = responseText.match(/```(?:json)?\s*([\s\S]*?)```/);
                if (jsonMatch) jsonStr = jsonMatch[1];
                const parsed = JSON.parse(jsonStr.trim());
                
                if (parsed.summary) summary = parsed.summary;
                if (parsed.insights) insights = parsed.insights;
                console.log(`✅ [Qwen AI] Successfully derived insights and returned analytics data!`);
            } catch (err) {
                console.error("❌ [Qwen AI] Failed to generate AI insights:", err);
                summary = "Failed to generate AI insights due to an error.";
            }
        } else {
             insights = [
                 { label: "New Forms", value: "0", insightText: "No data available yet." }
             ];
        }

        res.json({
            success: true,
            data: {
                liveUsers,
                totalVisitors,
                submissionsCount: submissions.length,
                summary,
                insights,
                businessName: website.name
            }
        });

    } catch (error) {
        console.error("[Analytics] Error:", error);
        res.status(500).json({ success: false, message: "Internal server error" });
    }
};
