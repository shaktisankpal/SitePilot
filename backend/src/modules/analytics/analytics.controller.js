const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({
    model: "gemini-3-flash-preview", // Note: Using 3-flash-preview as standard in this project, this aligns with the available Gemini high performance models
});

const ollamaClient = new Ollama({
    host: process.env.OLLAMA_HOST || 'http://localhost:11434'
});

/**
 * GET /api/analytics/dashboard
 * Returns dashboard stats for current tenant
 */
export const getDashboardStats = async (req, res) => {
    const tenantId = req.tenantId;

    const [websiteCount, pageCount, aiUsageCount, deploymentCount, userCount, recentActivity, recentDeployments] =
        await Promise.all([
            Website.countDocuments({ tenantId }),
            Page.countDocuments({ tenantId }),
            AIUsageLog.countDocuments({ tenantId }),
            Deployment.countDocuments({ tenantId }),
            User.countDocuments({ tenantId, isActive: true }),
            ActivityLog.find({ tenantId })
                .populate("userId", "name email")
                .sort({ createdAt: -1 })
                .limit(20)
                .lean(),
            Deployment.find({ tenantId })
                .populate("deployedBy", "name email")
                .populate("websiteId", "name")
                .sort({ createdAt: -1 })
                .limit(10)
                .lean(),
        ]);

    res.json({
        success: true,
        stats: {
            websiteCount,
            pageCount,
            aiUsageCount,
            deploymentCount,
            userCount,
        },
        recentActivity,
        recentDeployments,
    });
};

/**
 * GET /api/analytics/activity
 */
export const getActivityLogs = async (req, res) => {
    const { page = 1, limit = 20 } = req.query;
    const skip = (parseInt(page) - 1) * parseInt(limit);

    const [logs, total] = await Promise.all([
        ActivityLog.find({ ...req.tenantFilter })
            .populate("userId", "name email")
            .sort({ createdAt: -1 })
            .skip(skip)
            .limit(parseInt(limit))
            .lean(),
        ActivityLog.countDocuments({ ...req.tenantFilter }),
    ]);

    res.json({ success: true, logs, total, page: parseInt(page), limit: parseInt(limit) });
};

/**
 * GET /api/analytics/website/:websiteId
 * Returns advanced analytics generated by AI based on form submissions
 */
export const getWebsiteAnalytics = async (req, res) => {
    try {
        const { websiteId } = req.params;
        const tenantId = req.tenantId;

        // Verify website ownership
        const website = await Website.findOne({ _id: websiteId, tenantId }).populate("tenantId");
        if (!website) {
            return res.status(404).json({ success: false, message: "Website not found or access denied" });
        }

        // Generate slugs
        const tenantSlug = website.tenantId.slug || tenantId;
        const websiteSlug = website.slug || website.name?.toLowerCase().replace(/[^a-z0-9]+/g, '-') || websiteId;

        // Fetch form submissions
        console.log(`\n📡 [Analytics] Fetching remote forms data from Firebase for ${website.name}...`);
        const submissions = await firebaseService.getFormSubmissions(tenantSlug, websiteSlug, 500);
        console.log(`🔥 [Firebase] Successfully loaded ${submissions.length} dynamic form submissions!`);

        // --- REAL visitor stats from Firebase + Socket.IO ---
        // Total page views stored persistently in Firestore
        const { totalPageViews } = await firebaseService.getAnalyticsTotals(tenantSlug, websiteSlug);
        const totalVisitors = totalPageViews;

        // Live visitors = sockets currently in the visitor room for this websiteId
        const websiteIdStr = website._id.toString();
        const liveUsers = liveVisitors.get(websiteIdStr)?.size ?? 0;

        let insights = [];
        let summary = "Not enough data for insights.";

        if (submissions.length > 0) {
            const prompt = `
You are an advanced data analytics AI. Given the following form submissions from a business website, generate a structured analytics insight report. Think about what the business is and what kind of metrics make sense (e.g. juices sold, popular flavors, etc).

Form Submissions Data:
${JSON.stringify(submissions.slice(0, 100))} // Limit to 100 for token limits

Return a JSON object matching this structure EXACTLY. No markdown formatting, just raw JSON:
{
  "summary": "A 2 sentence summary of business performance.",
  "insights": [
    { "label": "Most Popular X", "value": "Result", "insightText": "Detailed insight" }
  ]
}
`;
            // Try the fast local model first; fall back to the cloud model so insights
            // generate seamlessly even if the local model is unavailable.
            let responseText = "";
            try {
                console.log(`🧠 [AI] Analyzing ${submissions.length} submissions...`);
                const result = await ollamaClient.chat({
                    model: 'qwen3.5:4b',
                    messages: [
                        { role: 'system', content: 'You are an advanced data analytics AI. You output strictly in JSON.' },
                        { role: 'user', content: prompt }
                    ],
                    format: 'json',
                    think: false,
                    options: { temperature: 0.1 }
                });
                responseText = result.message.content;
            } catch (primaryErr) {
                console.warn("[AI] Local model unavailable, using fallback:", primaryErr.message);
                try {
                    const result = await model.generateContent(prompt);
                    responseText = result.response.text();
                } catch (fallbackErr) {
                    console.error("[AI] Insight generation failed:", fallbackErr.message);
                }
            }

            if (responseText) {
                try {
                    let jsonStr = responseText;
                    const jsonMatch = responseText.match(/```(?:json)?\s*([\s\S]*?)```/);
                    if (jsonMatch) jsonStr = jsonMatch[1];
                    const parsed = JSON.parse(jsonStr.trim());
                    if (parsed.summary) summary = parsed.summary;
                    if (parsed.insights) insights = parsed.insights;
                    console.log(`✅ [AI] Derived insights from submissions.`);
                } catch (parseErr) {
                    console.error("[AI] Failed to parse insights JSON:", parseErr.message);
                    summary = "Could not generate insights from the latest submissions.";
                }
            } else {
                summary = "Could not generate insights right now. Please try again shortly.";
            }
        } else {
             insights = [
                 { label: "New Forms", value: "0", insightText: "No data available yet." }
             ];
        }

        res.json({
            success: true,
            data: {
                liveUsers,
                totalVisitors,
                submissionsCount: submissions.length,
                summary,
                insights,
                businessName: website.name
            }
        });

    } catch (error) {
        console.error("[Analytics] Error:", error);
        res.status(500).json({ success: false, message: "Internal server error" });
    }
};

/**
 * GET /api/analytics/website/:websiteId/submissions
 * Returns the raw form submissions (lead data) for the website owner/admin.
 */
export const getFormSubmissions = async (req, res) => {
    try {
        const { websiteId } = req.params;
        const tenantId = req.tenantId;

        const website = await Website.findOne({ _id: websiteId, tenantId }).populate("tenantId");
        if (!website) return res.status(404).json({ success: false, message: "Website not found or access denied" });

        const tenantSlug = website.tenantId.slug || tenantId;
        const websiteSlug = website.slug || website.name?.toLowerCase().replace(/[^a-z0-9]+/g, "-") || websiteId;

        const submissions = await firebaseService.getFormSubmissions(tenantSlug, websiteSlug, 500);
        return res.json({ success: true, submissions, count: submissions.length });
    } catch (error) {
        console.error("[Analytics] Form submissions error:", error);
        return res.status(500).json({ success: false, message: "Failed to load form submissions" });
    }
};
